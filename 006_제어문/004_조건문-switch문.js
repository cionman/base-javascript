// 004 조건문 - switch 문
/*
    - switch 문은 주어진 표현식을 평가하여 그 값과 일치하는 표현식을 갖는 case 문으로 실행흐름을 옮긴다.
    - case 문은 상황을 의미하는 표현식을 지정하고 콜론으로 마친다.
    - if else 문으로 해결할 수 있다면 switch보다 if else문을 사용하는 편이 좋다. 하지만 조건이 너무 많아서 switch를 사용했을때
    가독성이 더 좋다면 switch문이 좋다.

  switch(표현식) {

      case 표현식1:
        switch문의 표현식과 표현식1이 일치하면 실행될 문
        break;
      case 표현식2:
        switch문의 표현식과 표현식2이 일치하면 실행될 문
        break;
      default:
        switch문의 표현식과 일치하는 case문이 없을때 실행되는 문

  }


 */