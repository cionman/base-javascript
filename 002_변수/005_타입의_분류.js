/*
- 자바스크립트는 크게 두가지로 나눌 수 있음, 원시타입과 객체타입
- 원시타입 :
    - 숫자, 문자열, 논리값, undefined, null, 심벌(Symbol)
    - 데이터를 구성하는 가장 기본적인 요소로 불변값으로 정의
    - 원시 타입의 값은 원시 값, 원시 값을 변수에 대입하면 변수에 그 값이 저장됨
- 객체 타입 :
    - 원시타입에 속하지 않은 자바스크립트의 값은 객체
    - 객체는 변수 여러개가 모여서 만들어진 복합 데이터 타입
    - 객체 안에 저장된 값은 바꿀 수 있음
    - 객체는 참조 타입
    - 객체 타입의 값을 변수에 대입하면 변수는 객체에 대한 참조(메모리 주소 정보)가 할당
    - 배열,함수, 정규표현식 등이 객체
 */
