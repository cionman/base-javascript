/*
    - 객체리터럴
        - 생성자 함수: Object
        - 프로토타입 : Object.prototype
    - 함수리터럴
        - 생성자 함수 : Function
        - 프로토타입 : Function.prototype
    - 배열리터럴
        - 생성자 함수 : Array
        - 프로토타입 : Array.prototype
    - 정규표현식 리터럴
        - 생성자 함수 : RegExp
        - 프로토타입 : RegExp.prototype

    - 프로토타입과 생성자 함수는 단독으로 존재할 수 없고 언제나 쌍으로 존재한다.
 */