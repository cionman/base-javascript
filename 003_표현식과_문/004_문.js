// 004 문

/*
  - 문(statement) 프로그램을 구성하는 기본 단위이자 최소 실행단위다. 문의 집합으로 이뤄진 것이 바로 프로그램이며, 문을 작성하고 순서에 맞게 나열하는 것이 프로그래밍이다.
  - 문은 여러 토큰으로 구성된다.
  - 토큰이란 문법적인 의미를 가지며, 문법적으로 더 이상 나눌 수 없는 코드의 기본요소를 의미한다.
  - 문은 명령문이라고도 부른다.
  - 문은 선언문, 할당문, 조건문, 반복문등으로 구분할 수 있다.
  - 변수 선언문을 실행하면 변수가 선언되고,
    할당문을 실행하면 값이 할당된다.
    조건문을 실행하면 지정한 조건에 따라 실행할 코드 블록이 결정되어 실행되고,
    반복문을 실행하면 특정 코드 블록이 반복 실행된다.
 */